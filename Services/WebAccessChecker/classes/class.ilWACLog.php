<?php
require_once('./Services/Logging/classes/class.ilLog.php');

/**
 * Class ilWACLog
 *
 * @author  Fabian Schmid <fs@studer-raimann.ch>
 * @version 1.0.0
 */
class ilWACLog extends ilLog {

	/**
	 * @var ilWACLog
	 */
	protected static $instance;


	/**
	 * @return ilWACLog
	 */
	public static function getInstance() {
		if (! isset(self::$instance)) {
			self::$instance = new self('/var/iliasdata/trunk/', 'wac.log');;
		}

		return self::$instance;
	}


	/**
	 * @param      $a_msg
	 * @param null $a_log_level
	 */
	public function write($a_msg, $a_log_level = NULL) {
		parent::write($a_msg, $a_log_level); // TODO: Change the autogenerated stub
	}
}

?>
